package eu.mszulc.datasources;

public class Color
{
    private final float r, g, b;

    public Color(float r, float g, float b)
    {
        this.r = r;
        this.g = g;
        this.b = b;

    }

    public float getR()
    {
        return r;
    }

    public float getG()
    {
        return g;
    }

    public float getB()
    {
        return b;
    }

}
